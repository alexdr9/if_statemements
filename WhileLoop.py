# while loops will continue to repeat for as long as a certain condition is true

i = 1                          # sets i as a variable and is equal to 1
while i <= 10:                 # creates a while loop, as long as i is less then or equal to 10 then the loop continues
    print(i)
    i += 1                     # (or,   i = i + 1)  adds 1 to variable per loop

print("Loop completed")
