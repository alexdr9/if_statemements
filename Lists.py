instruments = ["Guitar", "Bass", "Drums", "Keyboard"]  # stores strings, numbers & booleans in a list called instruments
    # index       0        1        2         3

print(instruments[3])                             # prints the fourth index item from instruments list

print(instruments[-2])                            # prints the second index items in from the right side

print(instruments[1:])                            # prints items from list from index position 1 onwards

print(instruments[1:3])                # prints list items from position 1 up to 3 (but not including 3)

instruments[0] = "Vocals"              # changes item in position 0 to new string
print(instruments[0])                  # prints item in position 0 of list





